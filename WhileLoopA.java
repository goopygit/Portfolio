public class WhileLoopA{
   public static void main(String[] args) {
   
   int count = 0; //set initial value to 0
   System.out.println("BEGIN");
   while (count < 10) { // while loop, to terminate when count hits 10
      System.out.println(count);
      count ++; //increase variable by 1 each iteration
   }
   System.out.println("END");
   }
   
}